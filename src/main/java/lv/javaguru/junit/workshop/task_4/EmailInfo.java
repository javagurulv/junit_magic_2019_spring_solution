package lv.javaguru.junit.workshop.task_4;

public class EmailInfo {

    private int year;
    private double income;


    public EmailInfo(int year, double income) {
        this.year = year;
        this.income = income;
    }

    public int getYear() {
        return year;
    }

    public double getIncome() {
        return income;
    }
}
